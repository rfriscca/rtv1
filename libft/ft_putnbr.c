#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int	n2;

	if (n >= 0)
		n2 = n;
	if (n < 0)
	{
		ft_putchar('-');
		n2 = -n;
	}
	if (n2 > 9)
	{
		ft_putnbr(n2 / 10);
		ft_putnbr(n2 % 10);
	}
	if (n2 <= 9)
		ft_putchar(n2 + 48);
}
